const fs = require("fs");
const path = require("path");

const ROOT_DIR = path.resolve(__dirname, "..");

const CHAIN_ID = process.env.CHAIN_ID || "31337";

const broadcastPath = path.join(
  ROOT_DIR,
  "foundry",
  "broadcast",
  "DeployMscMarketV1.s.sol",
  CHAIN_ID,
  "run-latest.json",
);

const outputDir = path.join(ROOT_DIR, "frontend", "src", "constants");
const outputPath = path.join(outputDir, "contracts.ts");

if (!fs.existsSync(broadcastPath)) {
  throw new Error(`Broadcast file not found: ${broadcastPath}`);
}

const broadcast = JSON.parse(fs.readFileSync(broadcastPath, "utf8"));

const transactions = broadcast.transactions || [];

// 找 ERC1967Proxy 的部署交易
const proxyTx = transactions.find((tx) => {
  return (
    tx.contractName === "ERC1967Proxy" ||
    tx.contractName === "TransparentUpgradeableProxy"
  );
});

if (!proxyTx || !proxyTx.contractAddress) {
  console.log(
    "Available transactions:",
    transactions.map((tx) => ({
      contractName: tx.contractName,
      contractAddress: tx.contractAddress,
    })),
  );

  throw new Error("Proxy contract address not found in broadcast file.");
}

const proxyAddress = proxyTx.contractAddress;

fs.mkdirSync(outputDir, { recursive: true });

const content = `// Auto-generated by scripts/sync-contract-address.js
// Do not edit manually.

export const CONTRACT_ADDRESSES = {
  mscMarketV1: "${proxyAddress}",
} as const;

export const MSC_MARKET_V1_ADDRESS = CONTRACT_ADDRESSES.mscMarketV1;
`;

fs.writeFileSync(outputPath, content);

console.log(`Synced MscMarketV1 proxy address: ${proxyAddress}`);
console.log(`Written to: ${outputPath}`);
